    private static Map<String, String> sExtensionMap = new HashMap<String, String>(50);

    public static String guessMimeType(String filename) {
        return guessMimeType(filename, null);
    }

    public static String guessMimeType(String filename, String defaultValue) {
        if (filename == null)
            return defaultValue;
        int dot = filename.lastIndexOf('.');
        if (dot == -1 || dot == filename.length() - 1)
            return defaultValue;
        String extension = filename.substring(dot + 1).toLowerCase();
        // XXX: special-case for foo.doc.bak?
        String guess = sExtensionMap.get(extension);
        return guess == null ? defaultValue : guess;
    }


    public static void addExtensionMapping(String extension, String contentType) {
        addExtensionMapping(extension, contentType, true);
    }

    public static void addExtensionMapping(String extension, String contentType, boolean overrideExisting) {
        extension = extension.toLowerCase();
        if (overrideExisting || !sExtensionMap.containsKey(extension))
            sExtensionMap.put(extension, contentType);
    }

